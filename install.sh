#!/usr/bin/env bash
# =============================================================================
# Classroom Display — Self-Extracting Install Script
# Raspberry Pi OS Trixie (Debian 13), 64-bit
# ONE FILE. Just run:  bash install.sh
# =============================================================================
set -euo pipefail

GREEN='\033[0;32m'; YELLOW='\033[1;33m'; RED='\033[0;31m'; NC='\033[0m'
info() { echo -e "${GREEN}[INFO]${NC}  $*"; }
warn() { echo -e "${YELLOW}[WARN]${NC}  $*"; }
die()  { echo -e "${RED}[ERR]${NC}   $*" >&2; exit 1; }

APP_USER="${USER}"
APP_DIR="${HOME}/classroom-server"
VENV_DIR="${APP_DIR}/venv"
SERVICE_NAME="classroom-display"
PORT=80

info "Classroom Display — Self-Extracting Installer"
info "App directory : ${APP_DIR}"
info "Running as    : ${APP_USER}"
info "Port          : ${PORT}"
echo

# ── 1. System packages ────────────────────────────────────────────────────────
info "Updating package lists…"
sudo apt-get update -qq

info "Installing system dependencies…"
sudo apt-get install -y -qq \
    python3 \
    python3-venv \
    python3-pip \
    authbind \
    unzip \
    curl

# authbind: lets the app bind port 80 without running as root
sudo touch /etc/authbind/byport/${PORT}
sudo chmod 755 /etc/authbind/byport/${PORT}
sudo chown "${APP_USER}" /etc/authbind/byport/${PORT}
info "authbind configured for port ${PORT}"

# ── 2. Extract embedded files ─────────────────────────────────────────────────
info "Extracting application files…"
mkdir -p "${APP_DIR}"/{static,cache,backups,templates}

TMPZIP="$(mktemp /tmp/classroom-XXXXXX.zip)"
# Find the line where the payload starts (everything after __PAYLOAD__)
PAYLOAD_LINE=$(grep -n "^__PAYLOAD__$" "$0" | cut -d: -f1)
tail -n +"$((PAYLOAD_LINE + 1))" "$0" | base64 -d > "${TMPZIP}"

# Extract — only overwrite server.py and templates (never touches data files)
unzip -q -o "${TMPZIP}" server.py -d "${APP_DIR}/"
unzip -q -o "${TMPZIP}" "templates/*" -d "${APP_DIR}/"
rm -f "${TMPZIP}"
info "Extracted server.py → ${APP_DIR}/server.py"
info "Extracted templates → ${APP_DIR}/templates/"

# ── 3. Python virtual environment ─────────────────────────────────────────────
if [[ -d "${VENV_DIR}" ]]; then
    info "Virtual environment already exists — upgrading packages…"
else
    info "Creating Python virtual environment…"
    python3 -m venv "${VENV_DIR}"
fi

info "Installing Python packages…"
"${VENV_DIR}/bin/pip" install --quiet --upgrade pip
"${VENV_DIR}/bin/pip" install --quiet flask requests werkzeug waitress

info "Python packages installed:"
"${VENV_DIR}/bin/pip" show flask requests werkzeug \
    | grep -E "^(Name|Version):" \
    | paste - - \
    | awk '{printf "  %-12s %s\n", $2, $4}'

# ── 4. systemd service ────────────────────────────────────────────────────────
info "Writing systemd service unit…"
sudo tee /etc/systemd/system/${SERVICE_NAME}.service > /dev/null << EOF
[Unit]
Description=Classroom Display Server
After=network-online.target
Wants=network-online.target

[Service]
Type=simple
User=${APP_USER}
WorkingDirectory=${APP_DIR}
ExecStart=/usr/bin/authbind --deep ${VENV_DIR}/bin/python3 ${APP_DIR}/server.py
Restart=always
RestartSec=5
StandardOutput=journal
StandardError=journal
SyslogIdentifier=classroom-display
Environment=PORT=${PORT}
TimeoutStopSec=10

[Install]
WantedBy=multi-user.target
EOF

sudo systemctl daemon-reload
sudo systemctl enable ${SERVICE_NAME}
info "systemd service enabled"

# Nightly restart timer (3 AM) — flushes memory, ensures clean daily start
sudo tee /etc/systemd/system/${SERVICE_NAME}-nightly.service > /dev/null << EOFUNIT
[Unit]
Description=Classroom Display Nightly Restart

[Service]
Type=oneshot
ExecStart=/bin/systemctl restart ${SERVICE_NAME}
EOFUNIT

sudo tee /etc/systemd/system/${SERVICE_NAME}-nightly.timer > /dev/null << EOFUNIT
[Unit]
Description=Classroom Display Nightly Restart at 3 AM

[Timer]
OnCalendar=*-*-* 03:00:00
Persistent=true

[Install]
WantedBy=timers.target
EOFUNIT

sudo systemctl daemon-reload
sudo systemctl enable ${SERVICE_NAME}-nightly.timer
sudo systemctl start  ${SERVICE_NAME}-nightly.timer
info "Nightly restart timer enabled (3 AM daily)"

# ── 5. Convenience alias ──────────────────────────────────────────────────────
ALIAS_LINE="alias classroom-logs='journalctl -u ${SERVICE_NAME} -f'"
if ! grep -qF "classroom-logs" "${HOME}/.bashrc" 2>/dev/null; then
    echo "${ALIAS_LINE}" >> "${HOME}/.bashrc"
    info "Added 'classroom-logs' alias to ~/.bashrc"
fi

# ── 6. Start (or restart) the service ────────────────────────────────────────
if systemctl is-active --quiet ${SERVICE_NAME}; then
    info "Restarting service to pick up new files…"
    sudo systemctl restart ${SERVICE_NAME}
else
    info "Starting service…"
    sudo systemctl start ${SERVICE_NAME}
fi
sleep 2

# ── 7. Summary ────────────────────────────────────────────────────────────────
echo
echo -e "${GREEN}═══════════════════════════════════════════════════${NC}"
if systemctl is-active --quiet ${SERVICE_NAME}; then
    echo -e "${GREEN}  ✓ Classroom Display is running!${NC}"
    echo -e "${GREEN}═══════════════════════════════════════════════════${NC}"
    echo
    echo "  Admin panel : http://$(hostname -I | awk '{print $1}')/admin"
    echo "  Logs        : journalctl -u ${SERVICE_NAME} -f"
    echo "  (or type)   : classroom-logs    (reload shell first)"
else
    echo -e "${RED}  ✗ Service failed to start${NC}"
    echo -e "${GREEN}═══════════════════════════════════════════════════${NC}"
    echo
    warn "Check logs with:  journalctl -u ${SERVICE_NAME} -n 50"
fi
echo

# Script must exit before the payload or bash will try to execute it
exit 0
